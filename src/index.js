const reducer = (state = initState, action) => {
  switch (action.type) {
    case 'INCREMENT': return { count: state.count + 1 }
    case 'SEND_MESSAGE': 
      let { user_index, message, icon}  = action.payload;
      let { listUsers, n_rand } = state;
      // console.log(listUsers[user_index].listMessage.length)
      listUsers[user_index].listMessage.push({
          type: "me",
          message: message ?? "",
          icon: icon ?? "",
          time: getCurrentTime()
      })
      listUsers[user_index].listMessage.push({
                type: "!me",
                messages: [
                    {
                        message: "V√¢ng ·∫°",
                        icon: "",
                        time: getCurrentTime()
                    }
                ]
            })
      n_rand = Math.random();
      return {...state,listUsers, n_rand}
      
    default: return state
  }
}
function getCurrentTime() {
    const now = new Date();

    const hours = String(now.getHours()).padStart(2, '0');
    const minutes = String(now.getMinutes()).padStart(2, '0');
    const seconds = String(now.getSeconds()).padStart(2, '0'); // th√™m gi√¢y
    const day = String(now.getDate()).padStart(2, '0');
    const month = String(now.getMonth() + 1).padStart(2, '0'); // Th√°ng b·∫Øt ƒë·∫ßu t·ª´ 0
    const year = now.getFullYear();

    return `${hours}:${minutes}:${seconds} ${day}/${month}/${year}`;
}





